#include "3-calc.h"

/**
 * op_add - a function that adds two ints
 * @a: first int
 * @b: second int
 * Return: returns the value of the operation
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function that subtracts two ints
 * @a: first int
 * @b: second int
 * Return: returns the value of the operation
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function that multiplies two ints
 * @a: first int
 * @b: second int
 * Return: returns the value of the operation
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function that divides two ints
 * @a: first int
 * @b: second int
 * Return: returns the value of the operation
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function that modulo two ints
 * @a: first int
 * @b: second int
 * Return: returns the value of the operation
 */

int op_mod(int a, int b)
{
	return (a % b);
}
