/**
 * mul - function prototype
 *
 * Description: takes in int a and int b and multiples
 * multplying them and giving the result
 *
 * @a: first input
 * @b: second input
 *
 * Return: returns result of multiplication
 */

int mul(int a, int b)
{
	int result;

	result = a * b;

	return (result);
}

