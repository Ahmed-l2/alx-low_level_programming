#include <stdio.h>

/**
 * main - entry point
 * Description: program that prints the number of arguments passed into it.
 * @argc: number of arguments passed
 * @argv: arugments passed
 * Return: prints number of arguments
 */

int main(int argc, char *argv[])
{
	argv = argv;
	printf("%d\n", argc);
	return (0);
}
